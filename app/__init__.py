import os

from flask import Flask

from config import Config


def create_app(config_class=Config):
    """Flask 工厂函数

    创建并配置 Flask 应用实例，然后注册路由蓝图。
    使用工厂模式的好处：
      - 可以创建多个独立的 app 实例（如测试环境）
      - 配置与 app 创建解耦
      - 后续扩展（如初始化数据库）只需在工厂函数中添加
    """
    # ---------------------------------------------------------------
    # 1. 创建 Flask 实例
    # ---------------------------------------------------------------
    app = Flask(__name__)

    # ---------------------------------------------------------------
    # 2. 加载配置
    # ---------------------------------------------------------------
    # from_object() 会把 Config 类中所有 大写 属性读入 app.config 字典
    # 之后在路由中通过 app.config["UPLOAD_FOLDER"] 即可获取配置值
    app.config.from_object(config_class)

    # ---------------------------------------------------------------
    # 3. 确保上传目录存在
    # ---------------------------------------------------------------
    # 如果 uploads/ 目录不存在就创建它，防止保存文件时因目录缺失而报错
    os.makedirs(app.config["UPLOAD_FOLDER"], exist_ok=True)

    # ---------------------------------------------------------------
    # 4. 注册蓝图（路由）
    # ---------------------------------------------------------------
    # 导入路由模块并注册蓝图
    # 使用蓝图的好处：路由可分组、可加 URL 前缀、可复用
    from app.routes import main_bp
    app.register_blueprint(main_bp)

    # ---------------------------------------------------------------
    # 5. 返回应用实例
    # ---------------------------------------------------------------
    return app
